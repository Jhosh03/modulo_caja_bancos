    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
     
        C_cuenta R_cuenta = new C_cuenta();
        PERSONA per = new PERSONA();
        cuenta cu = new cuenta();
        String correo = new String(txtCorreo.getText());
        
        if (!txtCodigo.getText().equals("") && !txtNombre.getText().equals("")
                && !txtApellido.getText().equals("") && !txtCorreo.getText().equals("")
                && !txtNcuenta.getText().equals("") && !txtNomCuenta.getText().equals("")&&!txtSaldo.getText().equals("")) {
            if (cu.esEmail(correo)) {
                
            per.setId(txtCodigo.getText());
            per.setNombre(txtNombre.getText());
            per.setApellido(txtApellido.getText());
            per.setCorreo(txtCorreo.getText());
            per.setPassword("null");
            
            cu.setId(txtNcuenta.getText());
            cu.setNom_cuenta(txtNomCuenta.getText());
            cu.setPersona(txtCodigo.getText());
            cu.setSaldo(txtSaldo.getText());           
            if(R_cuenta.R_cuenta(per, cu)==1){
            JOptionPane.showMessageDialog(null,"Se ha registrado la cuenta"); 
            limpiar();
            }          
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un correo electronico valido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los datos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

