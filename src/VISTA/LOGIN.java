    private void btn_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IngresarActionPerformed

        C_LOGIN ingresar = new C_LOGIN();
        String pass = new String(txtPassword.getPassword());
        
        if (!txtUsuario.getText().equals("") && !txtPassword.getPassword().equals("")) {

            if (ingresar.Ingresar(txtUsuario.getText(), pass)!=0) {
                
                if (ingresar.Ingresar(txtUsuario.getText(), pass)==1) {
                ADMIN mostrar = new ADMIN();
                mostrar.setVisible(true);
                this.setVisible(false);
                }
                if (ingresar.Ingresar(txtUsuario.getText(), pass)==2) {
                ADMINISTRADOR mostrar = new ADMINISTRADOR();
                mostrar.setVisible(true);
                this.setVisible(false);
                }
               if (ingresar.Ingresar(txtUsuario.getText(), pass)==3) {
                vista_colaborador mostrar = new vista_colaborador();
                mostrar.setVisible(true);
                this.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar sus datos");
        }                
      
    }//GEN-LAST:event_btn_IngresarActionPerformed

