     void estado_cuenta (String id_cuenta) {
        String sql = "select ID_TRANSACCION,ID_CUENTA,NOMBRE_CUENTA,MOVIMIENTO,MONTO,DESCRIPCION,FECHA \n" +
"from cuenta_transaccion CT inner join transaccion T \n" +
"on CT.TRANSACCION_ID_TRANSACCION=T.ID_TRANSACCION inner join cuenta C \n" +
"on CT.CUENTA_ID_CUENTA=C.ID_CUENTA where C.ID_CUENTA=?";
        try {
            cn = con.getConexion();
            ps = cn.prepareStatement(sql);
            ps.setString(1, id_cuenta);
            rs = ps.executeQuery();
            Object[] ROL = new Object[7];
            md = (DefaultTableModel) tblEstado_cuenta.getModel();
            while (rs.next()) {
                ROL[0] = rs.getInt("ID_TRANSACCION");
                ROL[1] = rs.getString("ID_CUENTA");
                ROL[2] = rs.getString("NOMBRE_CUENTA");
                ROL[3] = rs.getString("MOVIMIENTO");
                ROL[4] = rs.getInt("MONTO");
                ROL[5] = rs.getString("DESCRIPCION");
                ROL[6] = rs.getString("FECHA");
                md.addRow(ROL);
            }
            tblEstado_cuenta.setModel(md);
            cn.close();
        } catch (SQLException e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(null," cuenta no encontrada");
        }
        
    }
     void saldo(String id_cuenta){
        
          String sql="select saldo from cuenta where ID_CUENTA= ?";
          try {
            cn = con.getConexion();
            ps = cn.prepareStatement(sql);
            ps.setString(1, id_cuenta);
            rs = ps.executeQuery();

            if (rs.next()) {
                lblsaldo_cuenta.setVisible(true);
                lblsaldo_cuenta.setText(rs.getString("saldo"));
            } else {
                JOptionPane.showMessageDialog(null, "Cuenta no encontrada");
            }
         cn.close();
        } catch (HeadlessException | SQLException e) {
            System.err.println(e);
        }      
      }
     
     void nombre(String id_cuenta){
        
          String sql="select nombre, apellido  from cuenta "
      + "CU inner join persona PE on CU.PERSONA_ID_PERSONA=PE.ID_PERSONA where ID_CUENTA =?";
          try {
            cn = con.getConexion();
            ps = cn.prepareStatement(sql);
            ps.setString(1, id_cuenta);
            rs = ps.executeQuery();

            if (rs.next()) {
                lblNomP.setVisible(true);
                lblApPr.setVisible(true);
                lblNomP.setText(rs.getString("nombre"));
                lblApPr.setText(rs.getString("apellido"));
                
            } else {
                JOptionPane.showMessageDialog(null, "Cuenta no encontrada");
            }
         cn.close();
        } catch (HeadlessException | SQLException e) {
            System.err.println(e);
        }      
      }
    
